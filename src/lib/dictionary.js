// 単語の原形候補を生成（簡易レンマタイザ）
function lemmas(word) {
  const w = word.toLowerCase();
  const out = [w];
  if (w.endsWith('ies'))  out.push(w.slice(0, -3) + 'y');
  if (w.endsWith('ves'))  out.push(w.slice(0, -3) + 'f');
  if (w.endsWith('es'))   out.push(w.slice(0, -2));
  if (w.endsWith('s') && !w.endsWith('ss')) out.push(w.slice(0, -1));
  if (w.endsWith('ied'))  out.push(w.slice(0, -3) + 'y');
  if (w.endsWith('ed'))   { out.push(w.slice(0, -2)); out.push(w.slice(0, -1)); }
  if (w.endsWith('ing'))  { out.push(w.slice(0, -3)); out.push(w.slice(0, -3) + 'e'); }
  if (w.endsWith('ly'))   out.push(w.slice(0, -2));
  if (w.endsWith('tion')) out.push(w.slice(0, -4) + 'te');
  if (w.endsWith('ment')) out.push(w.slice(0, -4));
  if (w.endsWith('ness')) out.push(w.slice(0, -4));
  if (w.endsWith('ity'))  out.push(w.slice(0, -3) + 'e');
  if (w.endsWith('ation'))out.push(w.slice(0, -5) + 'e');
  if (w.endsWith('ment')) out.push(w.slice(0, -4) + 'e');
  return out;
}

/**
 * 文中の単語を辞書＋用語集で引く
 * @param {string} sentence  英文
 * @param {Array}  glossary  [{en, ja, note}]
 * @returns {Array} [{word, ja, source}]  source: 'glossary' | 'dict'
 */
export function lookupWords(sentence, glossary = []) {
  // トークン化（句読点除去）
  const tokens = sentence.match(/[A-Za-z][A-Za-z'-]*/g) || [];
  const seen = new Set();
  const results = [];

  // 用語集をマップ化（先に検索）
  const glMap = {};
  for (const g of glossary) glMap[g.en.toLowerCase()] = g;

  for (const raw of tokens) {
    const lower = raw.toLowerCase();
    if (STOP.has(lower)) continue;
    if (lower.length < 3) continue;
    if (seen.has(lower)) continue;
    seen.add(lower);

    // 用語集チェック（完全一致）
    if (glMap[lower]) {
      results.push({ word: raw, ja: glMap[lower].ja, source: 'glossary' });
      continue;
    }

    // 辞書チェック（レンマタイズ）
    const forms = lemmas(raw);
    let found = null;
    for (const f of forms) {
      if (DICT[f]) { found = DICT[f]; break; }
    }
    if (found) {
      results.push({ word: raw, ja: found, source: 'dict' });
    }
  }

  return results;
}
